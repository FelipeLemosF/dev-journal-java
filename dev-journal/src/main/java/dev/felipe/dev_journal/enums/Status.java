package dev.felipe.dev_journal.enums;

/**
 * Exemplo básico de Enum de Status
 *
 * <p>Utilizado para indicar o estado de algo
 * <ul>
 *     <li>{@link #ATIVO} - está sendo utilizado</li>
 *     <li>{@link #INATIVO} - não está sendo utilizado</li>
 *     <li>{@link #PENDENTE} - aguardando ativação</li>
 * </ul>
 * </p>
 *
 */
public enum Status {
    ATIVO,
    INATIVO,
    PENDENTE
}
